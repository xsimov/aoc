package main

import "testing"

func TestAlgorithm(t *testing.T) {
	var cases = []int{
		0, 1, 2, 0, 1, 2, 0, 1, 2,
		3, 4, 5, 3, 4, 5, 3, 4, 5,
		6, 7, 8, 6, 7, 8, 6, 7, 8,
		9, 10, 11, 9, 10, 11, 9, 10, 11,
		12, 13, 14, 12, 13, 14, 12, 13, 14,
		15, 16, 17, 15, 16, 17, 15, 16, 17,
		18, 19, 20, 18, 19, 20, 18, 19, 20,
		21, 22, 23, 21, 22, 23, 21, 22, 23,
		24, 25, 26, 24, 25, 26, 24, 25, 26,
		27, 28, 29, 27, 28, 29, 27, 28, 29,
		30, 31, 32, 30, 31, 32, 30, 31, 32,
		33, 34, 35, 33, 34, 35, 33, 34, 35,
		36, 37, 38, 36, 37, 38, 36, 37, 38,
	}

	for i, tc := range cases {
		if f(i) != tc {
			t.Errorf("expected f(%v) to return %v, but returned %v instead", i, tc, f(i))
		}
	}
}

func f(i int) int {
	return i%3 + (i / 9 * 3)
}
